interface ScannedFile {
  path: string
  name: string
  lines: number
  sizeBytes: number
  category: 'claude-md' | 'rules' | 'skills' | 'settings' | 'other'
}

/** CC 構成ファイルの検出対象パターン */
const SCAN_TARGETS = [
  { glob: 'CLAUDE.md', category: 'claude-md' as const },
  { glob: '.claude/settings.json', category: 'settings' as const },
  { glob: '.claude/settings.local.json', category: 'settings' as const },
]

const SCAN_DIRS = [
  { dir: '.claude/rules', category: 'rules' as const },
  { dir: '.claude/skills', category: 'skills' as const },
]

/** 指定パスの CC 構成ファイルをスキャン */
export async function scanProject(projectPath: string): Promise<ScannedFile[]> {
  const results: ScannedFile[] = []

  // 単体ファイルチェック
  for (const target of SCAN_TARGETS) {
    const fullPath = join(projectPath, target.glob)
    if (existsSync(fullPath)) {
      const content = await readFile(fullPath, 'utf-8')
      const info = await stat(fullPath)
      results.push({
        path: fullPath,
        name: target.glob,
        lines: content.split('\n').length,
        sizeBytes: info.size,
        category: target.category,
      })
    }
  }

  // ディレクトリスキャン
  for (const { dir, category } of SCAN_DIRS) {
    const dirPath = join(projectPath, dir)
    if (!existsSync(dirPath)) continue
    try {
      const entries = await readdir(dirPath, { withFileTypes: true })
      for (const entry of entries) {
        if (!entry.isFile()) continue
        const fullPath = join(dirPath, entry.name)
        const content = await readFile(fullPath, 'utf-8')
        const info = await stat(fullPath)
        results.push({
          path: fullPath,
          name: `${dir}/${entry.name}`,
          lines: content.split('\n').length,
          sizeBytes: info.size,
          category,
        })
      }
    } catch {
      // ディレクトリ読み取り失敗は無視
    }
  }

  // ~/.claude/ もスキャン
  const userClaudeDir = join(app.getPath('home'), '.claude')
  if (existsSync(userClaudeDir) && !projectPath.includes('.claude')) {
    for (const target of SCAN_TARGETS) {
      const name = basename(target.glob)
      const fullPath = join(userClaudeDir, name)
      if (existsSync(fullPath) && !results.some((r) => r.path === fullPath)) {
        const content = await readFile(fullPath, 'utf-8')
        const info = await stat(fullPath)
        results.push({
          path: fullPath,
          name: `~/.claude/${name}`,
          lines: content.split('\n').length,
          sizeBytes: info.size,
          category: target.category,
        })
      }
    }

    for (const { dir, category } of SCAN_DIRS) {
      const dirName = dir.replace('.claude/', '')
      const dirPath = join(userClaudeDir, dirName)
      if (!existsSync(dirPath)) continue
      try {
        const entries = await readdir(dirPath, { withFileTypes: true })
        for (const entry of entries) {
          if (!entry.isFile()) continue
          const fullPath = join(dirPath, entry.name)
          if (results.some((r) => r.path === fullPath)) continue
          const content = await readFile(fullPath, 'utf-8')
          const info = await stat(fullPath)
          results.push({
            path: fullPath,
            name: `~/.claude/${dirName}/${entry.name}`,
            lines: content.split('\n').length,
            sizeBytes: info.size,
            category,
          })
        }
      } catch {
        // skip
      }
    }
  }

  return results
}

