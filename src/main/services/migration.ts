/** Conversion Pack MD を生成 */
export async function generateConversionPack(
  scannedFiles: ScannedFile[]
): Promise<string> {
  const lang: Language = getConfig().language

  // スキャンファイルセクションを構築
  const fileSections: string[] = []
  for (const file of scannedFiles) {
    const content = await readFile(file.path, 'utf-8')
    const categoryLabel = lang === 'ja' ? 'カテゴリ' : 'Category'
    const linesLabel = lang === 'ja' ? '行数' : 'Lines'
    const sizeLabel = lang === 'ja' ? 'サイズ' : 'Size'
    fileSections.push(`### ${file.name}`)
    fileSections.push(`- ${categoryLabel}: ${file.category}`)
    fileSections.push(`- ${linesLabel}: ${file.lines}`)
    fileSections.push(`- ${sizeLabel}: ${file.sizeBytes} bytes`)
    fileSections.push('')
    fileSections.push('```')
    fileSections.push(content)
    fileSections.push('```')
    fileSections.push('')
  }
  const scannedFilesSection = fileSections.join('\n')

  const generatedAt = new Date().toISOString()
  const claudeMdFile = scannedFiles.find(f => f.name === 'CLAUDE.md')
  const sourceLines = claudeMdFile ? claudeMdFile.lines : 0

  if (lang === 'ja') {
    return cpTemplateJa(CCPIT_VERSION, GITHUB_URL, CLAUDE_CODE_DOCS, scannedFilesSection, generatedAt, sourceLines)
  }
  return cpTemplateEn(CCPIT_VERSION, GITHUB_URL, CLAUDE_CODE_DOCS, scannedFilesSection, generatedAt, sourceLines)
}

interface ParsedBlock {
  filename: string
  targetPath: string
  content: string
}

/** claude.ai 出力 MD からコードブロックをパースし、配置先を判定 */
export function parseImportMd(mdContent: string): ParsedBlock[] {
  const blocks: ParsedBlock[] = []
  const headingRegex = /^#{1,4}\s+(?:P\d:\s+)?(?:rules\/|skills\/)?(.+)/
  const codeBlockRegex = /```(?:markdown|json|yaml)?\n([\s\S]*?)```/g

  const lines = mdContent.split('\n')

  // 各行の開始位置を記録
  const lineStarts: number[] = []
  let pos = 0
  for (const line of lines) {
    lineStarts.push(pos)
    pos += line.length + 1
  }

  // コードブロック抽出
  let match: RegExpExecArray | null
  while ((match = codeBlockRegex.exec(mdContent)) !== null) {
    const blockContent = match[1]
    const matchPos = match.index

    // このコードブロックの直前の見出しを探す
    let heading = ''
    for (let i = 0; i < lineStarts.length; i++) {
      if (lineStarts[i] > matchPos) break
      const hm = lines[i].match(headingRegex)
      if (hm) heading = hm[1].trim()
    }

    const { filename, targetPath } = resolveTarget(heading)
    blocks.push({ filename, targetPath, content: blockContent })
  }

  return blocks
}

function resolveTarget(
  heading: string
): { filename: string; targetPath: string } {
  const lower = heading.toLowerCase()

  // CLAUDE.md
  if (lower.includes('claude.md') || lower.includes('p1')) {
    return { filename: 'CLAUDE.md', targetPath: 'CLAUDE.md' }
  }

  // rules/
  if (lower.includes('rules/') || lower.includes('p2')) {
    const nameMatch = heading.match(/rules\/(.+\.md)/i)
    const name = nameMatch ? nameMatch[1] : `rule_${Date.now()}.md`
    return { filename: name, targetPath: `rules/${name}` }
  }

  // skills/
  if (lower.includes('skills/') || lower.includes('p3')) {
    const nameMatch = heading.match(/skills\/(.+?)(?:\/SKILL\.md)?$/i)
    const name = nameMatch ? nameMatch[1] : `skill_${Date.now()}`
    return { filename: `${name}/SKILL.md`, targetPath: `skills/${name}/SKILL.md` }
  }

  // settings.json deny
  if (lower.includes('settings') || lower.includes('deny') || lower.includes('p0')) {
    return { filename: 'settings_deny_additions.json', targetPath: 'settings_deny_additions.json' }
  }

  return { filename: basename(heading) || 'unknown.md', targetPath: heading }
}

/** パースした結果を Golden に配置 */
export async function importToGolden(
  blocks: ParsedBlock[],
  goldenBasePath: string,
  templateName: string
): Promise<{ placed: string[]; errors: string[] }> {
  const result = { placed: [] as string[], errors: [] as string[] }

  for (const block of blocks) {
    const destPath = join(goldenBasePath, templateName, block.targetPath)
    const destDir = join(destPath, '..')

    try {
      await mkdir(destDir, { recursive: true })
      await writeFile(destPath, block.content, 'utf-8')
      result.placed.push(block.targetPath)
    } catch (err) {
      result.errors.push(
        `${block.targetPath}: ${err instanceof Error ? err.message : String(err)}`
      )
    }
  }

  return result
}

// ─── .pit ファイル インポート機能 ───

export interface PitEntry {
  /** ZIP 内のパス (e.g. "CLAUDE.md", "rules/grep-exhaustive.md") */
  path: string
  content: string
  lines: number
}

export interface PitPreview {
  entries: PitEntry[]
  claudeMdPreview: string
  claudeMdLines: number
  rulesCount: number
  skillsCount: number
  coverageMapSummary: { totalRows: number; uncoveredCount: number } | null
  metricsRaw: string | null
  validationErrors: string[]
}

/** YAML フロントマターの存在を簡易検証 */
function hasYamlFrontmatter(content: string): boolean {
  return content.startsWith('---\n') && content.indexOf('\n---', 4) !== -1
}

/** .pit ファイルを読み込み・検証し、プレビューデータを返す */
export function importPitFile(filePath: string): PitPreview {
  const buf = readFileSync(filePath)
  const zip = new AdmZip(buf)
  const zipEntries = zip.getEntries()

  const entries: PitEntry[] = []
  const validationErrors: string[] = []

  for (const entry of zipEntries) {
    if (entry.isDirectory) continue
    const content = entry.getData().toString('utf-8')
    entries.push({
      path: entry.entryName.replace(/\\/g, '/'),
      content,
      lines: content.split('\n').length,
    })
  }

  // --- 構造検証 ---

  // CLAUDE.md 必須
  const claudeMd = entries.find((e) => e.path === 'CLAUDE.md')
  if (!claudeMd) {
    validationErrors.push('CLAUDE.md が .pit 内に存在しません')
  }

  // coverage-map.md 必須
  const coverageMap = entries.find((e) => e.path === 'coverage-map.md')
  if (!coverageMap) {
    validationErrors.push('coverage-map.md が .pit 内に存在しません')
  }

  // metrics.yaml 必須
  const metricsEntry = entries.find((e) => e.path === 'metrics.yaml')
  if (!metricsEntry) {
    validationErrors.push('metrics.yaml が .pit 内に存在しません')
  }

  // rules/*.md に YAML フロントマターがあるか
  const rulesEntries = entries.filter((e) => e.path.startsWith('rules/') && e.path.endsWith('.md'))
  for (const rule of rulesEntries) {
    if (!hasYamlFrontmatter(rule.content)) {
      validationErrors.push(`${rule.path}: YAML フロントマターがありません`)
    }
  }

  // skills/*/SKILL.md に YAML フロントマターがあるか
  const skillsEntries = entries.filter((e) => e.path.match(/^skills\/.+\/SKILL\.md$/))
  for (const skill of skillsEntries) {
    if (!hasYamlFrontmatter(skill.content)) {
      validationErrors.push(`${skill.path}: YAML フロントマターがありません`)
    }
  }

  // カバレッジマップのサマリー解析
  let coverageMapSummary: PitPreview['coverageMapSummary'] = null
  if (coverageMap) {
    const tableRows = coverageMap.content.split('\n').filter((line) => line.startsWith('|') && !line.includes('---'))
    // ヘッダー行を除く
    const dataRows = tableRows.length > 1 ? tableRows.length - 1 : 0
    const uncoveredCount = coverageMap.content.split('\n').filter(
      (line) => line.toLowerCase().includes('未カバー') || line.toLowerCase().includes('uncovered')
    ).length
    coverageMapSummary = { totalRows: dataRows, uncoveredCount }
  }

  // CLAUDE.md の冒頭 10 行プレビュー
  const PREVIEW_LINE_COUNT = 10
  const claudeMdPreview = claudeMd
    ? claudeMd.content.split('\n').slice(0, PREVIEW_LINE_COUNT).join('\n')
    : ''

  return {
    entries,
    claudeMdPreview,
    claudeMdLines: claudeMd?.lines ?? 0,
    rulesCount: rulesEntries.length,
    skillsCount: skillsEntries.length,
    coverageMapSummary,
    metricsRaw: metricsEntry?.content ?? null,
    validationErrors,
  }
}

/** 配置対象エントリ（coverage-map.md, metrics.yaml は除外） */
const DEPLOY_EXCLUDES = ['coverage-map.md', 'metrics.yaml']

/** .pit の内容を ~/.claude/ に配置 */
export async function deployPitFile(
  entries: PitEntry[]
): Promise<{ deployed: string[]; backedUp: string[]; errors: string[] }> {
  const userClaudeDir = join(app.getPath('home'), '.claude')
  const deployed: string[] = []
  const backedUp: string[] = []
  const errors: string[] = []

  const deployEntries = entries.filter((e) => !DEPLOY_EXCLUDES.includes(e.path))

  for (const entry of deployEntries) {
    const destPath = join(userClaudeDir, entry.path)
    const destDir = dirname(destPath)

    try {
      // バックアップ
      if (existsSync(destPath)) {
        const bakPath = `${destPath}.bak`
        if (!existsSync(bakPath)) {
          await copyFile(destPath, bakPath)
          backedUp.push(entry.path)
        }
      }

      await mkdir(destDir, { recursive: true })
      await writeFile(destPath, entry.content, 'utf-8')
      deployed.push(entry.path)
    } catch (err) {
      errors.push(`${entry.path}: ${err instanceof Error ? err.message : String(err)}`)
    }
  }

  return { deployed, backedUp, errors }
}
